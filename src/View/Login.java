    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
    String username = Loginusername.getText();
    String password = new String (Loginpassword.getPassword()).trim();

    // quick validation
    if (username == null || username.trim().isEmpty() || password == null || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Username dan password ga boleh kosong.", "WOY", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // call model
    cekLogin loginModel = new cekLogin();
    cekLogin.AuthResult result = loginModel.authenticate(username, password);

    if (result == null || result.getRole() == cekLogin.Role.NONE) {
        JOptionPane.showMessageDialog(this, "Username atau password salah.", "Login Gagal", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // success â€” open appropriate dashboard
    switch (result.getRole()) {
        case ADMIN:
            new DashboardSA(result.getId()).setVisible(true);
            break;
        case DOSEN:
            new dashboardDosen(result.getId()).setVisible(true);
            break;
        case MAHASISWA:
            new DashboardMahasiswa(result.getId()).setVisible(true);
            break;
        default:
            return;
    }

    // close login window
    this.dispose();
    }//GEN-LAST:event_btnLoginActionPerformed

